package balancebite.model;

/**
 * Enum representing the different roles a user can have in the system.
 * This is used to assign and manage user permissions and access levels.
 */
public enum Role {

    /**
     * Regular user role with limited access to certain functionalities.
     */
    USER,

    /**
     * Admin role with elevated permissions and access to management features.
     */
    ADMIN
}
