package balancebite.model.user;

/**
 * Enum representing the various roles a user can have in the system.
 */
public enum UserRole {
    /**
     * Represents a user role.
     */
    USER,

    /**
     * Represents an owner user role.
     */
    CHEF,

    /**
     * Represents an admin user role.
     */
    ADMIN
}
